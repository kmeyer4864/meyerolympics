// Pre-generated Sudoku puzzles
// Each puzzle has a grid (0 = empty cell) and its solution
// Clue counts: Easy = 45, Medium = 40, Hard = 35

export type Difficulty = 'easy' | 'medium' | 'hard'

export interface SudokuPuzzle {
  id: string
  difficulty: Difficulty
  grid: number[][]  // 0 represents empty cell
  solution: number[][]
}

// Helper to create a puzzle from strings (easier to read/maintain)
function parsePuzzle(id: string, difficulty: Difficulty, gridStr: string, solutionStr: string): SudokuPuzzle {
  const parseGrid = (str: string): number[][] => {
    const digits = str.replace(/\D/g, '')
    const grid: number[][] = []
    for (let i = 0; i < 9; i++) {
      grid.push(digits.slice(i * 9, (i + 1) * 9).split('').map(Number))
    }
    return grid
  }

  return {
    id,
    difficulty,
    grid: parseGrid(gridStr),
    solution: parseGrid(solutionStr),
  }
}

