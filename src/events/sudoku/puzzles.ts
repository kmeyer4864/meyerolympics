// Get puzzles by difficulty
export function getPuzzlesByDifficulty(difficulty: Difficulty): SudokuPuzzle[] {
  return ALL_PUZZLES.filter(p => p.difficulty === difficulty)
}

// Get a specific puzzle by ID
export function getPuzzleById(id: string): SudokuPuzzle | undefined {
  return ALL_PUZZLES.find(p => p.id === id)
}

// Get a random puzzle of given difficulty
export function getRandomPuzzle(difficulty: Difficulty): SudokuPuzzle {
  const puzzles = getPuzzlesByDifficulty(difficulty)
  const randomIndex = Math.floor(Math.random() * puzzles.length)
  return puzzles[randomIndex]
}

// Validate if a grid matches its solution
export function validateGrid(grid: number[][], solution: number[][]): boolean {
  for (let row = 0; row < 9; row++) {
    for (let col = 0; col < 9; col++) {
      if (grid[row][col] !== solution[row][col]) {
        return false
      }
    }
  }
  return true
}

// Check if a move is valid (doesn't violate Sudoku rules)
export function isValidMove(grid: number[][], row: number, col: number, value: number): boolean {
  // Check row
  for (let c = 0; c < 9; c++) {
    if (c !== col && grid[row][c] === value) return false
  }

  // Check column
  for (let r = 0; r < 9; r++) {
    if (r !== row && grid[r][col] === value) return false
  }

  // Check 3x3 box
  const boxRow = Math.floor(row / 3) * 3
  const boxCol = Math.floor(col / 3) * 3
  for (let r = boxRow; r < boxRow + 3; r++) {
    for (let c = boxCol; c < boxCol + 3; c++) {
      if ((r !== row || c !== col) && grid[r][c] === value) return false
    }
  }

  return true
}

// Check if the grid is completely filled
export function isGridComplete(grid: number[][]): boolean {
  for (let row = 0; row < 9; row++) {
    for (let col = 0; col < 9; col++) {
      if (grid[row][col] === 0) return false
    }
  }
  return true
}
